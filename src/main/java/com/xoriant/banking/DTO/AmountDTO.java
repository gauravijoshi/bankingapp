package com.xoriant.banking.DTO;

public class AmountDTO {

	private double amount;
	
	public AmountDTO() {
		super();
	}
	public AmountDTO(double amount) {
		super();
		this.amount = amount;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	
	
}
